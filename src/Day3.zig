const std = @import("std");
const print = std.debug.print;

fn traverse(slope_x: usize, slope_y: usize) i32 {
    var x: usize = 0;
    var toks = std.mem.tokenize(input, "\n");

    var num_trees: i32 = 0;

    while (toks.next()) |line| : (x += slope_x) {
        if (line[x % (line.len - 1)] == '#') num_trees += 1;

        var skip: i32 = 0;
        while (skip < slope_y - 1) : (skip += 1) {
            _ = toks.next() orelse return num_trees;
        }
    }

    return num_trees;
}

pub fn main() anyerror!void {
    // Part One
    {
        print("Number of Trees: {d}\n", .{traverse(3, 1)});
    }

    // Part Two
    {
        var product: i32 = traverse(1, 1) * traverse(3, 1) * traverse(5, 1) * traverse(7, 1) * traverse(1, 2);
        print("Result: {d}\n", .{product});
    }
}

